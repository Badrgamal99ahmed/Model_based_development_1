color = input('enter color code (R, G, B, Y, W): ','s');
switch color
    case 'r'
        disp('Red');
    case 'g'
        disp('Grean');
    case 'b'
        disp('Blue');
    case 'y'
        disp('Yellow');
    case 'w'
        disp('Whait');
    otherwise 
        disp('Invalide color ');
end